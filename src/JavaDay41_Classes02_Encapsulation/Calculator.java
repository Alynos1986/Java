package JavaDay41_Classes02_Encapsulation;

public class Calculator {
   private int a;
   private int b;
   private int result;

    public int getA(){
        return a;
    }
    public void setA(int newA){
        a = newA;
    }
    public int getB(){
        return b;
    }
    public void setB(int newB){
        b = newB;
    }
    public int getResult(){
        return result;
    }
    public void setResult(int newResult){
        result = newResult;
    }
}
